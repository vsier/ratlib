static int test_rcrc_custom_and_predefined_match(void)
{
    uintmax_t custom = rcrc_calc(16, UINTMAX_C(0x1021), UINTMAX_C(0x0000), false, false, UINTMAX_C(0x0000), checkBuf, checkBufSize);
    TEST_CHECK(custom == rcrc_calcFromId(RCRC_16_XMODEM, checkBuf, checkBufSize));
    TEST_CHECK(custom == UINTMAX_C(0x31C3));

    return 0;
}

static int test_rcrc_incremental_matches_one_shot(void)
{
    rcrc state;
    rcrc_initFromId(&state, RCRC_32_ISO_HDLC);

    rcrc_add(&state, checkBuf, 3);
    rcrc_add(&state, checkBuf + 3, checkBufSize - 3);

    TEST_CHECK(rcrc_result(&state) == rcrc_calcFromId(RCRC_32_ISO_HDLC, checkBuf, checkBufSize));

    return 0;
}

static int test_rcrc_predefined_ids_are_dense(void)
{
    TEST_CHECK(RCRC_1_PARITY_BIT == (rcrc_id)0);
    TEST_CHECK(RCRC_3_GSM == RCRC_1_PARITY_BIT + 1);
    TEST_CHECK(RCRC_64_XZ > RCRC_1_PARITY_BIT);

    return 0;
}

int test_rcrc(void)
{
    TEST_RUN(test_rcrc_check_values);
    TEST_RUN(test_rcrc_custom_and_predefined_match);
    TEST_RUN(test_rcrc_incremental_matches_one_shot);
    TEST_RUN(test_rcrc_predefined_ids_are_dense);

    return 0;
}
